        COUNT(DISTINCT pl.log_id) AS total_proctoring_events,
        COUNT(CASE WHEN pl.severity = 'HIGH' THEN 1 END) AS high_severity_events
    FROM exam_results er
    JOIN exam_sessions es ON er.session_id = es.session_id
    LEFT JOIN proctoring_logs pl ON es.session_id = pl.session_id
    WHERE er.exam_id = p_exam_id;
