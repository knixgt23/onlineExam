  CREATE OR REPLACE EDITIONABLE TRIGGER "C##EXAM_ADMIN"."TRG_AUTO_GRADE_MCQ" 
BEFORE INSERT OR UPDATE ON student_answers
FOR EACH ROW
DECLARE
    v_question_type VARCHAR2(20);
    v_marks NUMBER;
    v_is_correct NUMBER;
BEGIN
    -- Get question type and marks
    SELECT q.question_type, q.marks 
    INTO v_question_type, v_marks
    FROM questions q
    WHERE q.question_id = :NEW.question_id;

    -- Auto-grade if MCQ or TRUE_FALSE and an option is selected
    IF v_question_type IN ('MCQ', 'TRUE_FALSE') AND :NEW.selected_option_id IS NOT NULL THEN
        -- Check if the selected option is correct
        BEGIN
            SELECT is_correct INTO v_is_correct
            FROM mcq_options
            WHERE option_id = :NEW.selected_option_id;

            -- Award marks if correct
            IF v_is_correct = 1 THEN
                :NEW.marks_obtained := v_marks;
            ELSE
                :NEW.marks_obtained := 0;
            END IF;

            -- Mark as evaluated
            :NEW.is_evaluated := 1;

        EXCEPTION
            WHEN NO_DATA_FOUND THEN
                :NEW.marks_obtained := 0;
                :NEW.is_evaluated := 1;
        END;
    ELSIF v_question_type = 'SUBJECTIVE' THEN
        -- Subjective answers need manual grading
        :NEW.marks_obtained := 0;
        :NEW.is_evaluated := 0;
    END IF;

EXCEPTION
    WHEN OTHERS THEN
        -- If any error, set marks to 0
        :NEW.marks_obtained := 0;
        :NEW.is_evaluated := 0;
END;

/
