--  DDL for View V_ACTIVE_EXAM_SESSIONS
--------------------------------------------------------

  CREATE OR REPLACE FORCE EDITIONABLE VIEW "C##EXAM_ADMIN"."V_ACTIVE_EXAM_SESSIONS" ("SESSION_ID", "EXAM_ID", "EXAM_TITLE", "STUDENT_ID", "STUDENT_NAME", "ROLL_NUMBER", "START_TIME", "STATUS", "IP_ADDRESS", "TOTAL_PROCTORING_EVENTS", "HIGH_SEVERITY_EVENTS", "UNREAD_ALERTS") AS 
  SELECT 
    es.session_id,
    es.exam_id,
    e.exam_title,
    s.student_id,
    u.full_name AS student_name,
    st.roll_number,
    es.start_time,
    es.status,
    es.ip_address,
    COUNT(DISTINCT pl.log_id) AS total_proctoring_events,
    SUM(CASE WHEN pl.severity = 'HIGH' THEN 1 ELSE 0 END) AS high_severity_events,
    COUNT(DISTINCT a.alert_id) AS unread_alerts
FROM exam_sessions es
JOIN exams e ON es.exam_id = e.exam_id
JOIN students st ON es.student_id = st.student_id
JOIN users u ON st.user_id = u.user_id
JOIN students s ON es.student_id = s.student_id
LEFT JOIN proctoring_logs pl ON es.session_id = pl.session_id
LEFT JOIN alerts a ON es.session_id = a.session_id AND a.is_read = 0
WHERE es.status = 'IN_PROGRESS'
GROUP BY es.session_id, es.exam_id, e.exam_title, s.student_id, 
         u.full_name, st.roll_number, es.start_time, es.status, es.ip_address
;
