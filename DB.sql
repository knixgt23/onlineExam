    p_selected_option_id IN NUMBER DEFAULT NULL,
    p_is_marked_for_review IN NUMBER DEFAULT 0,
    p_status OUT VARCHAR2,
    p_message OUT VARCHAR2
) AS
    v_answer_count NUMBER;
BEGIN
    -- Check if answer already exists
    SELECT COUNT(*) INTO v_answer_count
    FROM student_answers
    WHERE session_id = p_session_id AND question_id = p_question_id;

    IF v_answer_count > 0 THEN
        -- Update existing answer
        UPDATE student_answers
        SET answer_text = p_answer_text,
            selected_option_id = p_selected_option_id,
            is_marked_for_review = p_is_marked_for_review,
            answered_at = CURRENT_TIMESTAMP
        WHERE session_id = p_session_id AND question_id = p_question_id;
    ELSE
        -- Insert new answer
        INSERT INTO student_answers (
            answer_id, session_id, question_id, answer_text, 
            selected_option_id, is_marked_for_review
        ) VALUES (
            seq_answer_id.NEXTVAL, p_session_id, p_question_id, p_answer_text,
            p_selected_option_id, p_is_marked_for_review
        );
    END IF;

    COMMIT;
    p_status := 'SUCCESS';
    p_message := 'Answer saved successfully';

EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        p_status := 'ERROR';
        p_message := SQLERRM;
END;

