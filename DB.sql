  CREATE OR REPLACE EDITIONABLE TRIGGER "C##EXAM_ADMIN"."TRG_CALC_DURATION" 
BEFORE UPDATE OF end_time ON exam_sessions
FOR EACH ROW
DECLARE
    v_interval INTERVAL DAY TO SECOND;
BEGIN
    IF :NEW.end_time IS NOT NULL AND :OLD.end_time IS NULL THEN
        -- Calculate interval
        v_interval := :NEW.end_time - :NEW.start_time;

        -- Calculate total minutes
        :NEW.actual_duration_minutes := 
            EXTRACT(DAY FROM v_interval) * 24 * 60 +
            EXTRACT(HOUR FROM v_interval) * 60 +
            EXTRACT(MINUTE FROM v_interval);
    END IF;
END;

