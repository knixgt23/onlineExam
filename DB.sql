  CREATE OR REPLACE EDITIONABLE FUNCTION "C##EXAM_ADMIN"."FN_EXAM_COMPLETION_RATE" (
    p_session_id IN NUMBER
) RETURN NUMBER AS
    v_total_questions NUMBER;
    v_answered_questions NUMBER;
    v_completion_rate NUMBER;
    v_exam_id NUMBER;
BEGIN
    -- Get exam_id from session
    SELECT exam_id INTO v_exam_id
    FROM exam_sessions
    WHERE session_id = p_session_id;

    -- Get total questions
    SELECT COUNT(*) INTO v_total_questions
    FROM questions
    WHERE exam_id = v_exam_id;

    -- Get answered questions
    SELECT COUNT(*) INTO v_answered_questions
    FROM student_answers
    WHERE session_id = p_session_id;

    IF v_total_questions > 0 THEN
        v_completion_rate := (v_answered_questions / v_total_questions) * 100;
    ELSE
        v_completion_rate := 0;
    END IF;

    RETURN ROUND(v_completion_rate, 2);
END;

