--  DDL for Procedure SP_END_EXAM_SESSION
--------------------------------------------------------
set define off;

  CREATE OR REPLACE EDITIONABLE PROCEDURE "C##EXAM_ADMIN"."SP_END_EXAM_SESSION" (
    p_session_id IN NUMBER,
    p_force_submit IN NUMBER DEFAULT 0,
    p_status OUT VARCHAR2,
    p_message OUT VARCHAR2
) AS
    v_total_obtained NUMBER := 0;
    v_total_marks NUMBER;
    v_percentage NUMBER;
    v_grade VARCHAR2(2);
    v_pass_marks NUMBER;
    v_result_status VARCHAR2(20);
    v_exam_id NUMBER;
    v_student_id NUMBER;
    v_start_time TIMESTAMP;
    v_end_time TIMESTAMP;
    v_duration NUMBER;
    v_interval INTERVAL DAY TO SECOND;
BEGIN
    -- Get exam and student details
    SELECT exam_id, student_id, start_time 
    INTO v_exam_id, v_student_id, v_start_time
    FROM exam_sessions 
    WHERE session_id = p_session_id;

    -- Set end time
    v_end_time := CURRENT_TIMESTAMP;

    -- Calculate duration using interval
    v_interval := v_end_time - v_start_time;

    -- Extract total minutes from interval
    v_duration := EXTRACT(DAY FROM v_interval) * 24 * 60 +
                  EXTRACT(HOUR FROM v_interval) * 60 +
                  EXTRACT(MINUTE FROM v_interval);

    -- Update session end time and duration
    UPDATE exam_sessions
    SET end_time = v_end_time,
        actual_duration_minutes = v_duration,
        status = CASE WHEN p_force_submit = 1 THEN 'FORCE_SUBMITTED' ELSE 'COMPLETED' END
    WHERE session_id = p_session_id;

    -- Calculate total marks obtained
    SELECT COALESCE(SUM(marks_obtained), 0) INTO v_total_obtained
    FROM student_answers
    WHERE session_id = p_session_id;

    -- Get total marks and pass marks
    SELECT total_marks, pass_marks INTO v_total_marks, v_pass_marks
    FROM exams WHERE exam_id = v_exam_id;

    -- Calculate percentage
    v_percentage := (v_total_obtained / v_total_marks) * 100;

    -- Determine grade
    IF v_percentage >= 90 THEN v_grade := 'A+';
    ELSIF v_percentage >= 80 THEN v_grade := 'A';
    ELSIF v_percentage >= 70 THEN v_grade := 'B';
    ELSIF v_percentage >= 60 THEN v_grade := 'C';
    ELSIF v_percentage >= 50 THEN v_grade := 'D';
    ELSE v_grade := 'F';
    END IF;

    -- Determine pass/fail status
    IF v_total_obtained >= v_pass_marks THEN
        v_result_status := 'PASS';
    ELSE
        v_result_status := 'FAIL';
    END IF;

    -- Insert result
    INSERT INTO exam_results (
        result_id, session_id, student_id, exam_id, total_marks_obtained,
        percentage, grade, status, evaluated_at
    ) VALUES (
        seq_result_id.NEXTVAL, p_session_id, v_student_id, v_exam_id,
        v_total_obtained, v_percentage, v_grade, v_result_status, CURRENT_TIMESTAMP
    );

    COMMIT;
    p_status := 'SUCCESS';
    p_message := 'Exam submitted successfully. Score: ' || v_total_obtained || '/' || v_total_marks;

EXCEPTION
    WHEN OTHERS THEN
        ROLLBACK;
        p_status := 'ERROR';
        p_message := SQLERRM;
END;

