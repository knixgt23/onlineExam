--  DDL for Function FN_GET_TIME_REMAINING
--------------------------------------------------------

  CREATE OR REPLACE EDITIONABLE FUNCTION "C##EXAM_ADMIN"."FN_GET_TIME_REMAINING" (
    p_session_id IN NUMBER
) RETURN NUMBER AS
    v_start_time TIMESTAMP;
    v_duration_minutes NUMBER;
    v_elapsed_minutes NUMBER;
    v_remaining_minutes NUMBER;
    v_exam_id NUMBER;
BEGIN
    -- Get session start time and exam duration
    SELECT es.start_time, e.duration_minutes, es.exam_id
    INTO v_start_time, v_duration_minutes, v_exam_id
    FROM exam_sessions es
    JOIN exams e ON es.exam_id = e.exam_id
    WHERE es.session_id = p_session_id;

    -- Calculate elapsed time
    v_elapsed_minutes := ROUND((CURRENT_TIMESTAMP - v_start_time) * 24 * 60, 0);

    -- Calculate remaining time
    v_remaining_minutes := v_duration_minutes - v_elapsed_minutes;

    IF v_remaining_minutes < 0 THEN
        RETURN 0;
    ELSE
        RETURN v_remaining_minutes;
    END IF;
END;

